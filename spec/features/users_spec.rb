require 'rails_helper'

describe "Spec for Sign Up" do
  it "should create new user account" do
    visit new_user_registration_path
    email = "test1@gmail.com"
    fill_in 'user_name', :with => "abcdefg"
    fill_in 'user_email', :with => email
    fill_in 'user_password', :with => "password"
    fill_in 'user_password_confirmation', :with => "password"
    check 'user_terms'
    check 'user_global_subscribe'
    click_button 'Sign up'
    expect(page).to have_content  "a confirmation link has been sent to your email address"
  end
end